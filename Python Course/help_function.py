"""
Função help e seu uso.
"""

# Vamos pedir ajuda para a função print.
help(print)

# Como o help funciona?
# Ex.: help(round(-2.01))
''' 
Esse help irá retornar o resultado dessa função
que é -2, ou seja, uma integer, então o help
entende que o usuário quer ajuda com a definição
de integer
'''

help(round(-2.01))
