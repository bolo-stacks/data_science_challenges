# definindo as variáveis
x = 14
y = 16.254
z = True
k = not z
w = 'hello world'
my_number = "1.12321"

# imprimindo a variável e seu tipo
print('\nx =', x)
print(type(x), '\n')

# imprimindo a variável e seu tipo e arredondando
round_y = round(y)
print('y =', y)
print(type(y), '\n')
print('y redondo =', round_y)
print(type(round_y), '\n')

# imprimindo a variável e seu tipo
print('z =', z)
print(type(z), '\n')

# imprimindo a variável e seu tipo que é reversa de z (falsa)
print('k =', k)
print(type(k), '\n')

# imprimindo a variável e seu tipo e seu comprimento
print('w =', w)
print(type(w))
print(len(w), '\n')

# imprimindo a variável e seu tipo
print(my_number)
print(type(my_number), '\n')

# convertendo string para float
also_my_number = float(my_number)
print(also_my_number)
print(type(also_my_number))
